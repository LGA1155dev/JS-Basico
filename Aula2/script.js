//diferença na declaração de variaveis:

//variavel usando var é escopo de função 
var carro = 1;

//variavel usando let e const é escopo de bloco
//porém let é uma variavel feita para ser modificada ao decorrer do codigo
let carro = 2;

//escopo de bloco igual a let
//porém um vez que você declarar, não poderá mais modifica-la
const carro = 3;